package trab1.material;

public class MaterialEscolar{
    private int lapis = 0;
    private int caderno = 0;
    private int caneta = 0;

    public int getLapis() {
        return this.lapis;
    }

    public void setLapis(int lapis) {
        this.lapis = lapis;
    }

    public int getCaderno() {
        return this.caderno;
    }

    public void setCaderno(int caderno) {
        this.caderno = caderno;
    }

    public int getCaneta() {
        return this.caneta;
    }

    public void setCaneta(int caneta) {
        this.caneta = caneta;
    }
}
